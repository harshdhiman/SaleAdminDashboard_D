/**
 *
 * @param tokens Theme tokens
 * @returns reversed theme tokens
 */
function reverseTokens(tokens: { [key: string]: ColorShade }) {
  const reversedTokens = {};
  Object.entries(tokens).forEach(([key, val]) => {
    const keys = Object.keys(val);
    const values = Object.values(val);
    const length = keys.length;
    const reversedObj = {};
    for (let i = 0; i < length; i++) {
      (reversedObj as any)[keys[i]] = values[length - i - 1];
    }
    (reversedTokens as any)[key] = reversedObj;
  });
  return reversedTokens as {
    [key: string]: ColorShade;
  };
}

export const tokensLight = reverseTokens(tokensDark);

